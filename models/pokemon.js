// Definindo a classe Pokemon
class Pokemon {
    // Implementando o constructor da classe
    constructor(
        id, 
        number, 
        name, 
        type, 
        description, 
        weight, 
        height
    ) {
        this.id = id;
        this.number = number,
        this.name = name,
        this.type = type,
        this.description = description,
        this.weight = weight,
        this.height = height
    } 

}

module.exports = Pokemon